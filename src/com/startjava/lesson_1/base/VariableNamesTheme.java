package com.startjava.lesson_1.base;

public class VariableNamesTheme {
    public static void main(String[] args) {
        // 1. Разные переменные
        System.out.println("1. Разные переменные:");

        // цифра
        int digit = 4;
        System.out.println("цифра = " + digit);

        // сумма чисел
        int sum = 6 + 6;
        System.out.println("сумма чисел = " + sum);

        // произведение чисел
        int productNumber = 7 * 7;
        System.out.println("произведение чисел = " + productNumber);

        // максимальное число
        int maxNumber = 9;
        System.out.println("максимальное число = " + maxNumber);

        // количество десятков
        int countTens = 600 / 10;
        System.out.println("количество десятков = " + countTens);

        // вес собаки
        int dogWeight = 26;
        System.out.println("вес собаки = " + dogWeight + " кг");

        // исходное число
        int originalNumber = 0;
        System.out.println("исходное число = " + originalNumber);

        // процент по вкладу
        int interestRate = 16; // Используем целое число
        System.out.println("процент по вкладу = " + interestRate + "%");

        // символ &
        char ampersand = '&';
        System.out.println("символ & = " + ampersand);

        // код ошибки
        int errorCode = 500;
        System.out.println("код ошибки = " + errorCode);

        // тип сообщения
        String messageType = "Системное";
        System.out.println("тип сообщения = " + messageType);

        // число нулей
        int numberZeros = 0;
        System.out.println("число нулей = " + numberZeros);

        // уникальное число
        int uniqueNumber = 1;
        System.out.println("уникальное число = " + uniqueNumber);

        // случайное число
        int randomNumber = 333;
        System.out.println("случайное число = " + randomNumber);

        // математическое выражение
        String mathExpression = "2a + 2a";
        System.out.println("математическое выражение = " + mathExpression);

        // счет в игре
        String scoreGame = "6:9";
        System.out.println("счет в игре = " + scoreGame);

        // максимальная длина строки
        int maxStringLength = 49;
        System.out.println("максимальная длина строки = " + maxStringLength + " символов");

        // пункт меню
        int menuItem = 5;
        System.out.println("пункт меню = " + menuItem);

        // стоимость кофе на вынос
        int costCoffeeGo = 179; // Используем целое число
        System.out.println("стоимость кофе на вынос = " + costCoffeeGo + " рублей");

        // дата начала чего-либо
        String startDate = "2024-01-01";
        System.out.println("дата начала (чего-либо) = " + startDate);

        // окончание диапазона
        int endRange = 100;
        System.out.println("окончание диапазона = " + endRange);

        // полное имя работника месяца
        String fullNameEmployeeMonth = "Брайко Петр Олегович";
        System.out.println("полное имя работника месяца = " + fullNameEmployeeMonth);

        // заголовок электронной книги
        String ebookTitle = "Введение в программирование";
        System.out.println("заголовок электронной книги = " + ebookTitle);

        // вместимость (чего-либо)
        int capacity = 45;
        System.out.println("вместимость (чего-либо) = " + capacity + " литров");

        // счетчик
        int counter = 111;
        System.out.println("счетчик = " + counter);

        // путь до файла
        String pathFile = "c:\\мои документы\\example.txt";
        System.out.println("путь до файла = " + pathFile);

        // количество чисел в каждой строке
        int countNumbers = 100;
        System.out.println("количество чисел в каждой строке = " + countNumbers);

        System.out.println();

        // 2. Boolean-переменные
        System.out.println("2. Boolean-переменные:");

        // сотни равны?
        boolean areHundredsEqual = true;
        System.out.println("сотни равны? = " + areHundredsEqual);

        // компьютер включен?
        boolean isComputerOn = false;
        System.out.println("компьютер включен? = " + isComputerOn);

        // есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("есть равные цифры? = " + hasEqualDigits);

        // служба создана?
        boolean isServiceCreated = true;
        System.out.println("служба создана? = " + isServiceCreated);

        // файл пустой?
        boolean isFileEmpty = true;
        System.out.println("файл пустой? = " + isFileEmpty);

        // соединение активное?
        boolean isConnectionActive = true;
        System.out.println("соединение активное? = " + isConnectionActive);

        // новый?
        boolean isNew = false;
        System.out.println("новый? = " + isNew);

        // электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("электронная почта действительная? = " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println();

        // 3. Аббревиатуры
        System.out.println("3. Аббревиатуры:");

        // старый universally unique identifier
        String oldUUID = "3213-jnjhi";
        System.out.println("Старый UUID = " + oldUUID);

        // производитель оперативной памяти
        String ramManufacturer = "Kingston";
        System.out.println("Производитель оперативной памяти = " + ramManufacturer);

        // емкость жесткого диска
        int hddCapacity = 1024;
        System.out.println("Емкость жесткого диска = " + hddCapacity + " МБ");

        // протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста = " + http);

        // сокращенный uniform resource locator
        String shortUrl = "https://google.com ";
        System.out.println("Сокращенный URL = " + shortUrl);

        // новый идентификатор клиента
        int newClientId = 8996261;
        System.out.println("Новый идентификатор клиента = " + newClientId);

        // количество символов в ASCII
        int asciiSymbolCount = 128;
        System.out.println("Количество символов в ASCII = " + asciiSymbolCount);
    }
}